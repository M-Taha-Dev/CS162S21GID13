    private void PharmacyRequestTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PharmacyRequestTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) PharmacyRequestTable.getModel();
        int index = PharmacyRequestTable.getSelectedRow();
        int count = 0;
        String var = "";
        for (int i = 0; i < PharmacyManager.getInstance().requestLog.medReqList.size(); i++) {
            if (!PharmacyManager.getInstance().requestLog.medReqList.get(index).getReqStatus().equals("Added to Cart")) {
                count++;
            }
        }
        if (count > 0) {
           var = "" + model.getValueAt(index, 0);
        }
        MedicineRequest obj = PharmacyManager.getInstance().requestLog.getReqDetails(var);
        viewRequestID.setText(obj.getRequestId());
        viewPatientID.setText(obj.getPatientId());
        viewRequestDate.setText("" + obj.getReqDate());
        viewRequestedMedicine(obj.getMedReqList());
    }//GEN-LAST:event_PharmacyRequestTableMouseClicked

