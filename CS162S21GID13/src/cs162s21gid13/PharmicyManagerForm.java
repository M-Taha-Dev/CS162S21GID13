    private void MedicineSearchTableMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MedicineSearchTableMousePressed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) MedicineSearchTable.getModel();
        int rowIndex = MedicineSearchTable.getSelectedRow();
        String tag = "" + model.getValueAt(rowIndex, 0);
        int i = PharmacyManager.getInstance().medLog.getMedIndex(tag);
        if (i >= 0) {
            Medicine obj = PharmacyManager.getInstance().medLog.getMedicineDetails(i);
            EditNameField.setText(obj.getMedicineName());
            EditTagField.setText(obj.getMedicineTag());
            EditBestBefore.setText("" + obj.getExpiryDate());
            EditCostField.setText("" + obj.getMedicineCost());
        }
        EditPanel.setVisible(true);
    }//GEN-LAST:event_MedicineSearchTableMousePressed

    private void EditMedicineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditMedicineActionPerformed
        // TODO add your handling code here:
        if (EditMedicine.getText().equals("Edit")) {
            setEditTrue();
            EditMedicine.setText("Save");
        } else if (EditMedicine.getText().equals("Save")) {
            setEditFalse();
            EditMedicine.setText("Edit");
            EditPanel.setVisible(false);
            String var = EditNameField.getText();
            String tag = EditTagField.getText();
            String cost = EditCostField.getText();
            String exp = EditBestBefore.getText();

            Medicine obj = new Medicine();
            obj.setMedicineCost(Integer.parseInt(cost));
            obj.setMedicineName(var);
            obj.setMedicineTag(tag);
            int i = PharmacyManager.getInstance().medLog.getMedIndex(tag);
            if (i != -1) {
                PharmacyManager.getInstance().medLog.updateMedicine(i, obj);
            }

            List<Medicine> medList = new ArrayList<>();
            medList = PharmacyManager.getInstance().medLog.searchMedicineByName(var);
            //JOptionPane.showMessageDialog(null,medList.get(0).getMedicineName());
            createMedicineSearchTable(medList);

        }

    }//GEN-LAST:event_EditMedicineActionPerformed

